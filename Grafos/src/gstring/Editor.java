    private void mnuAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuAnalizarActionPerformed
        // Analizar
        palabras=txtEditor.getText().split("");
        for(int p=0;p<palabras.length;p++){
            if(Grafo.eside(palabras[p]))
                showMessageDialog(this, palabras[p]+" Es un identificador valido.");
            else
                showMessageDialog(this, palabras[p]+" No es un identificador valido.");
        }
    }//GEN-LAST:event_mnuAnalizarActionPerformed

